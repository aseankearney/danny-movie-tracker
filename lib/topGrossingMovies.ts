export function getAllTopGrossingMovies(startYear: number = 1989, endYear?: number): TopGrossingMovie[] {
  const currentYear = endYear || new Date().getFullYear()
  const allMovies: TopGrossingMovie[] = []
  
  for (let year = startYear; year <= currentYear; year++) {
    const movies = TOP_GROSSING_MOVIES_BY_YEAR[year] || []
    allMovies.push(...movies)
  }
  
  return allMovies
}

